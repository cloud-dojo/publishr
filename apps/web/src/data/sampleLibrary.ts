ファシリテーターは感情に敏感でいる必要がある。発言の内容だけでなく、声のトーン、表情、姿勢——それらが、その場の空気の変化を教えてくれる。

沈黙が続くとき、質問を変えてみる。同じ問いを繰り返しても答えは出ない。角度を変えた問いが、詰まった議論を動かす。

