異論を歓迎する空気がなければ、合意は同調にすぎない。健全な反対意見こそ、決定の質を上げる。

沈黙を恐れない。会議中の沈黙は、参加者が考えている証拠だ。その沈黙を10秒待つことで、表面的な答えではなく、本質的な答えが生まれることがある。

ファシリテーターは「進める人」ではなく「引き出す人」だ。自分の意見を出すのではなく、参加者の中にある考えを表に出すことに集中する。

会議でよく発言する人が必ずしも最も価値ある貢献をしているとは限らない。静かな人に積極的に問いを向けることが、多様な視点を引き出す。

