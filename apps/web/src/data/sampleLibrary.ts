決める会議が機能しない原因の多くは、事前準備の不足だ。情報が揃っていない、選択肢が整理されていない、関係者の意見が聞けていない——これらは会議の前に解決できる。

考える会議を有効にするには、問いの設計が全てだ。「どうすればいいか」という問いより「なぜうまくいっていないのか」という問いのほうが、思考を深める。問いが変われば、答えが変わる。

