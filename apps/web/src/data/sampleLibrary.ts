「今日の会議、どうでしたか」と参加者に一言聞くだけで、次回の改善ヒントが得られる。フィードバックなしに会議の質は上がらない。

会議のファシリテーションに慣れてきたら、自分以外のメンバーに任せてみる。ファシリテーターを経験することで、参加者としての振る舞いも変わる。全員がファシリテーターを経験できる組織が、最も成熟した会議文化を持つ。

沈黙を活かし、発言を分配し、異論を歓迎する——この三つを実践できるファシリテーターがいる会議は、人数が多くても密度が高くなる。

