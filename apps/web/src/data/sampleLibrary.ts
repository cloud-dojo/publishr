委譲とは放任ではない。任せた後に手を出さないためにこそ、失敗してよい範囲をあらかじめ決めておく。安全に失敗できる設計があれば、人は伸びる。

「自分がやった方が早い」は、短期では正しく、長期では必ず間違う。あなたが手を動かすほど、組織の上限はあなた一人の容量に固定される。

権限の設計図が引けたとき、あなたの仕事は「実行」から「設計」へと移る。現場が自分で回り始める瞬間を、あなたは少し寂しく、しかし誇らしく見届けることになる。

委譲の失敗の多くは、「任せた気になっていた」ことに気づかないところから始まる。任せたつもりで細部に口を出す。やり方を指定して結果だけを求める。それは委譲ではなく、責任の転嫁だ。

任せた仕事を「成功した」と評価するとき、その成功の原因を丁寧に言語化する。「運が良かった」で終わらせず、何がうまくいったかを相手と一緒に振り返る。その言語化が、次の任せ方を上手にする。

任せる相手の強みと弱みを把握しておく。強みのある領域に任せ、弱みのある領域は支援しながら任せる。どちらも理由なく任せると、不公平感が生まれる。

初めて任せる仕事は、小さく始める。最初から大きな権限を渡すより、小さな権限をうまく使えることを確認してから広げるほうが、双方にとって安全だ。

委譲後の確認はルールで行う。「週次で報告」「重要な判断は事前に相談」——ルールが決まっていれば、確認は不信の表れにならない。ルールなしの確認が、マイクロマネジメントに見える。

権限を渡す前に、その人が「何のために」その仕事をするのかを共有する。目的が腹落ちしている人は、細かく指示しなくても正しい方向に動く。目的なしの権限は、迷子を作るだけだ。

部下の失敗をあなたの失敗として受け止める姿勢が、委譲の土台を作る。失敗の責任を下に押し付けるリーダーの下では、誰も本気で挑戦しない。

「自分がやれば確実」という思い込みを疑う。あなたの「確実」は、あなたの経験に基づく。その経験のない人にとっては、別の「確実」があるかもしれない。多様なやり方を受け入れる柔軟性が、委譲を育てる。

権限は一度渡したら取り戻しにくい。取り戻す必要が生じたときは、正直に理由を説明する。「状況が変わったから」「自分の判断が浅かったから」——正直な理由は、関係を壊さない。

設計図を引くとは、現在の権限地図を描くことだ。誰が何を決められて、何を決められないか。曖昧な部分を白紙にしておかず、明文化する。明文化された権限は、組織の自律を支える柱になる。

委譲が進むにつれ、あなたの仕事は「守ること」から「育てること」に変わっていく。メンバーが育つほど、組織は強くなる。そして組織が強くなるほど、あなたはより大きな問いに集中できるようになる。

権限の設計図は、完成したら終わりではない。組織が変わるたびに引き直す。半年に一度、設計図を見直す習慣が、委譲の質を長期的に高めていく。

